pub trait Draw {
    fn draw(&self); // trait methods are by default public, unlike struct or enum methods
}

pub struct Screen {
    pub components: Vec<Box<dyn Draw>>,
}

impl Screen {
    pub fn run(&self) {
        for component in self.components.iter() {
            component.draw();
        }
    }
}

